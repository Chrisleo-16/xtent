export const getTranslation = (key: string, defaultValue: string, language: string = 'en') => {
  const keys = key.split('.');
  let value: any = translations[language as keyof typeof translations];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || defaultValue;
};

